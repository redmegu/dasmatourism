<h3>Frequently Asked Questions (FAQ)</h3>
<p>Here are answers to common questions about using the Dasmariñas Tourism Information System.</p>

<hr>

<h4 class="mt-4">1. How do I use the Interactive Map?</h4>
<p>The map is a static 2D view. You can pan (drag) the map and use the zoom controls (+ / –) in the bottom-right corner. Click on any colored pin (marker) to view quick information about an attraction or business.</p>

<h4 class="mt-4">2. Why can't I see my location on the map?</h4>
<p>The map is a static image and does not support real-time GPS or geolocation features. It is designed only for visual browsing and linking to detailed profiles, not for real-time navigation or route calculation.</p>

<h4 class="mt-4">3. How can I submit a review?</h4>
<p>You must first create a Regular User account and log in. Once logged in, navigate to any Attraction or Business profile page, where you will find the star rating and review form at the bottom of the page. Your review will be visible once approved by the Administrator.</p>

<h4 class="mt-4">4. I am a Business Owner, how do I list my business?</h4>
<p>You must register using the "Business Owner" role. Once registered and logged in, you will be directed to your Business Dashboard where you can submit a detailed profile, including permits and services, for validation by the City Tourism Office Administrator.</p>

<h4 class="mt-4">5. What is the Tourism Story Mode?</h4>
<p>The Story Mode is a visual novel-style feature that guides you through the history and culture of Dasmariñas City through a choice-driven, virtual tour narrative. It is an educational and interactive way to learn about the city's landmarks.</p>

