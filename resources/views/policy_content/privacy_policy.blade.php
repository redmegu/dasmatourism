<h3>Privacy Policy for Dasmariñas Tourism Information System</h3>

<p><strong>Effective Date:</strong> November 13, 2025</p>

<p>This Privacy Policy (the "Policy") outlines how the Tourism Information System for the City of Dasmariñas (the "System") collects, uses, protects, and handles your Personally Identifiable Information (PII) in compliance with the **Philippine Data Privacy Act of 2012 (Republic Act No. 10173)**.</p>

<p>By registering for or using the System, you consent to the data practices described in this Policy.</p>

<hr>

<h4>1. Information We Collect</h4>
<p>We collect information necessary to provide and secure our services:</p>

<ul>
    <li><strong>Identity & Contact Data:</strong> For user registration, authentication, and communication (Registered Users, Business Owners, Administrators).</li>
    <li><strong>User Activity Data:</strong> For personalized recommendations, analytics reports, and system optimization (All Users).</li>
    <li><strong>Business Data:</strong> For listing and verification in the Business Directory (Business Owners, Administrators).</li>
    <li><strong>Review & Rating Content:</strong> For moderation and display on public attraction pages (Registered Users).</li>
</ul>

<h4>2. Use of Your Information</h4>
<p>We use the collected data for the following purposes:</p>
<ul>
    <li><strong>Authentication and Access:</strong> To verify your identity and manage secure access to the respective dashboards.</li>
    <li><strong>Personalization:</strong> To provide personalized recommendations based on your interaction history and preferences.</li>
    <li><strong>Auditing and Reporting:</strong> To generate system usage analytics for the Tourism Office.</li>
    <li><strong>Content Moderation:</strong> To screen user-submitted reviews and content for appropriateness before public display.</li>
</ul>

<h4>3. Data Protection and Security</h4>
<p>We employ **AES-256 encryption** for sensitive data and implement reasonable security measures to protect against unauthorized access, alteration, disclosure, or destruction of your personal data, in accordance with the Data Privacy Act.</p>

<h4>4. Disclosure to Third Parties</h4>
<p>We do not sell, trade, or otherwise transfer your PII to outside parties. Data is shared internally only with the authorized System Administrator and the Tourism Office for operational and reporting purposes.</p>

<h4>5. Your Rights (Under RA 10173)</h4>
<p>You have the right to:</p>
<ul>
    <li><strong>Be Informed:</strong> Be notified and furnished with information about whether your personal data is being processed.</li>
    <li><strong>Access:</strong> Obtain a copy of any personal data we hold about you.</li>
    <li><strong>Rectification:</strong> Request correction of any inaccurate or outdated personal data.</li>
    <li><strong>Erasure or Blocking:</strong> Request the deletion or removal of your personal data from our system, subject to legal limitations.</li>
</ul>

<h4>6. Policy Changes</h4>
<p>Any changes to this Policy will be posted on this page with an updated "Effective Date." Continued use of the System after any change constitutes acceptance of the revised Policy.</p>